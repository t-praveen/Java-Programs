package typeCasting;

public class TypeCasting {

	public static void main(String[] args) {
		int x=4;
		double y=x;
		System.out.println(x);
		System.out.println(y);
		
		double myDouble=2.8642;
		int myInt=(int)myDouble;
		System.out.println(myDouble);
		System.out.println(myInt);

	}

}
