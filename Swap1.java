class Swap1
{
    public static void main(String args[])
    {
        int a=5;
        int b=4;
        int temp;
        System.out.println("Before Swapping, the numbers are:--");
        System.out.println("a:"+a+"\n"+"b:"+b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After Swapping, the numbers are:--");
        System.out.println("a:"+a+"\n"+"b:"+b);

    }
}